#print() = comando de saida
print("Hello World")

#Quando quiser guardar uma String (Frase)
nome = "Yago Lopes"

#Quando quiser gaurdar um número inteiro
idade = 22

#Exibir o meu nome (que está dentro da variável nome)
print(nome)
print(idade)

#Para exibir uma frase + o conteúdo que queira
print("Meu nome é " + nome+"\n")
print("Minha idade é "+str(idade)+" anos\n")
print(f"Minha idade é {idade} anos\n")
print("Minha idade é {} anos".format(idade))

#Para exibir uma frase + o conteúdo que queira usando o "format"
print("Meu nome é {} e tenho {} anos".format(nome,idade))

#Para exibir uma frase + o conteúdo que queira usando o "f"
print(f"Meu nome é {nome} e tenho {idade} anos")


